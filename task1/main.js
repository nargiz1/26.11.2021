"use strict"
while(true){
    let num=prompt("Enter number from 1 to 4:");
    switch(true){
        case (num==1):
            alert("Winter");
            break;
        case (num==2):
            alert("Spring");
            break;
        case (num==3):
            alert("Summer");
            break;
        case (num==4):
            alert("Fall");
            break;
    }
}

